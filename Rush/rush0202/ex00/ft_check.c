#include <unistd.h>


int	ft_check(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			write (0, "Error",5);
			return (1);
		}
		
		i++;
	}
	return (0);
}