#include <unistd.h>

void	ft_is_negative(int n)
{
	int	b;
	int	v;

	if (n >= 0)
	{
		b = 80;
		write(1, &b, 1);
	}
	else if (n < 0)
	{
		v = 78;
		write(1, &v, 1);
	}
}

